-- Initialize empty db
DROP DATABASE IF EXISTS rupaparkingdb;
CREATE DATABASE IF NOT EXISTS rupaparkingdb;
USE rupaparkingdb;

CREATE TABLE Users(
  username varchar(128),
  parkpass varchar(15),
  CONSTRAINT username_PK PRIMARY KEY(username)  
);

CREATE TABLE Lots(
    LotLetter varchar(3),
    MaxCapacity int(10),
    CurrentCapacity int(10),
    CONSTRAINT LotLetter_PK PRIMARY KEY(LotLetter)
);

CREATE TABLE Spots(
    LotLetter varchar(3),
    SpID varchar(5),
    isOpen boolean,
    CONSTRAINT LotLetter_SpID_CK PRIMARY KEY (LotLetter,SpID),
    CONSTRAINT LotLetter_FK FOREIGN KEY(LotLetter) REFERENCES Lots(LotLetter)
);

CREATE TABLE ParkHistory(
    hID int(32) AUTO_INCREMENT,
    Date timestamp DEFAULT CURRENT_TIMESTAMP,
    username varchar(128),
    LotLetter varchar(3),
    SpID varchar(5),
    isParked boolean,
    CONSTRAINT hID_Date_CK PRIMARY KEY(hID,Date),
    CONSTRAINT LotLetter_SpID_FK FOREIGN KEY(LotLetter,SpID) REFERENCES Spots(LotLetter,SpID),
    CONSTRAINT username_FK FOREIGN KEY(username) REFERENCES Users(username)
);

